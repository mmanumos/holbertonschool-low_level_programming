#include <stdio.h>
#include "3-calc.h"

/**
 * op_add - operate the values
 *
 * @a: given number
 * @b: given number
 *
 * Return: Result of operation
 *
 */

int op_add(int a, int b)
{
	return (a + b);
}


/**
 * op_sub - operate the values
 *
 * @a: given number
 * @b: given number
 *
 * Return: Result of operation
 *
 */

int op_sub(int a, int b)
{
	return (a - b);
}


/**
 * op_mul - operate the values
 *
 * @a: given number
 * @b: given number
 *
 * Return: Result of operation
 *
 */

int op_mul(int a, int b)
{
	return (a * b);
}



/**
 * op_div - operate the values
 *
 * @a: given number
 * @b: given number
 *
 * Return: Result of operation
 *
 */

int op_div(int a, int b)
{
	return (a / b);
}



/**
 * op_mod - operate the values
 *
 * @a: given number
 * @b: given number
 *
 * Return: Result of operation
 *
 */

int op_mod(int a, int b)
{
	return (a % b);
}
