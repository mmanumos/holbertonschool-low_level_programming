#include <stdio.h>

/**
 * main - Prints alphabet
 * letter: letter
 *
 * Description: Prints alphabet ascendently
 * Return: Always 0
 */


int main(void)
{
	char letter;

	for (letter = 'a'; letter <= 'z'; letter++)
	{
		putchar(letter);
		if (letter == 'z')
		{
			putchar('\n');
		}
	}
	return (0);
}
